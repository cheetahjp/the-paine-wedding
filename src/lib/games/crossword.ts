export type CrosswordDirection = "across" | "down";

export type CrosswordEntry = {
    id: string;
    answer: string;
    clue: string;
    direction: CrosswordDirection;
    row: number;
    col: number;
};

export type CrosswordCell = {
    row: number;
    col: number;
    key: string;
    answer: string | null;
    number: number | null;
    entryIds: string[];
};

export type CrosswordNumberedEntry = CrosswordEntry & {
    number: number;
    cells: string[];
};

export type CrosswordPuzzle = {
    id: string;
    title: string;
    subtitle: string;
    rows: number;
    cols: number;
    entries: CrosswordEntry[];
};

export type BuiltCrossword = Omit<CrosswordPuzzle, "entries"> & {
    cells: CrosswordCell[];
    entries: CrosswordNumberedEntry[];
    across: CrosswordNumberedEntry[];
    down: CrosswordNumberedEntry[];
};

export type CrosswordMetadata = {
    duration_seconds: number;
    checks_used: number;
    reveals_used: number;
    completed_at: string;
};

